export interface TransformOptions {
    /** Custom table class */
    tableClass?: string;
    /** Custom header class */
    headerCellsClass?: string;
    /** If header cells are bold */
    headerBold?: boolean;
    /** If header cells are italic */
    headerItalic?: boolean;
    /** If columns are sorteable */
    headerSorteable?: boolean;
    /** Custom body class */
    bodyCellsClass?: string;
    /** If body cells are bold */
    bodyBold?: boolean;
    /** If body cells are italic */
    bodyItalic?: boolean;
}
